public class Stacker<E> implements Stack<E> {
    private ArrayList<E> data  = new ArrayList<E>();
    
    @Override
    public void push(E item) {
        data.add(item);
    }

    @Override
    public E pull() {//regresa el valor de la cima y lo elimina del array
            return data.remove(data.size()-1);
    }

    @Override
    public E peek() {//muestra el  valor de la cima 
         return data.remove(data.size()-1);
    }

    @Override
    public boolean empty() {//true si esta vacia
        return data.isEmpty();
    }

    @Override
    public int size() {//regresa el tamano del stack
       return data.size();
    }
    
}
